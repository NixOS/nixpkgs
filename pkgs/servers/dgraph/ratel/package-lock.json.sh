#!/usr/bin/env nix-shell
#!nix-shell -I nixpkgs=../../../.. -i bash -p bash nodejs

set -e

rm -rf ratel deps.json node.nix node-packages.nix
cp -r $(nix-build --no-out-link -E "((import ../../../.. {}).callPackage ./default.nix {}).src") ratel
chmod -R u+w ratel
cd ratel/client
npm install
cp package-lock.json ../../
