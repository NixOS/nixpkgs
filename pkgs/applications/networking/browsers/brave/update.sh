#!/usr/bin/env nix-shell
#!nix-shell -i bash -p curl nix jq
# shellcheck shell=bash
#
# Regenerates pkgs/applications/networking/browsers/brave/packages/*.nix.
#
# Stable Brave is resolved via GitHub's /releases/latest endpoint, which by
# definition skips drafts and prereleases. Upstream tags a release either as
# stable, beta or nightly depending on which channel's builds it ships; there
# is no reliable signal in the release metadata beyond the asset filenames,
# so we identify each asset by its deterministic name pattern.

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" &>/dev/null && pwd)"

GH_AUTH=(${GITHUB_TOKEN:+-u ":$GITHUB_TOKEN"})
REPO="brave/brave-browser"

sri_hash() {
  nix-hash --to-sri --type sha256 "$(nix-prefetch-url --type sha256 "$1")"
}

# Emits a `<platform> = { url = ...; hash = ...; };` block if the release has
# an asset matching the given filename template. The generated Nix keeps
# `${version}` interpolations so subsequent bumps only refresh hashes.
#
# $1: release JSON (single object)
# $2: version (tag without leading "v")
# $3: asset filename template with literal ${version} placeholders
# $4: platform key to emit
emit_archive_entry() {
  local releaseJson="$1" version="$2" template="$3" platform="$4"
  local name="${template//\$\{version\}/$version}"

  if ! jq -e --arg n "$name" '.assets | map(.name) | index($n) != null' \
         <<<"$releaseJson" > /dev/null; then
    return 0
  fi

  local url="https://github.com/${REPO}/releases/download/v${version}/${name}"
  local hash
  hash="$(sri_hash "$url")"

  cat <<EOF
    ${platform} = {
      url = "https://github.com/${REPO}/releases/download/v\${version}/${template}";
      hash = "${hash}";
    };
EOF
}

# Writes pkgs/.../packages/<pname>.nix for one (pname, release) pair.
#
# $1: pname (e.g. "brave")
# $2: release JSON object
# $3: Linux .deb filename stem (e.g. "brave-browser")
# $4: macOS .zip filename stem (always "brave" for the regular browser; each
#     release tag uniquely identifies the channel)
write_package_nix() {
  local pname="$1" releaseJson="$2" debStem="$3" darwinStem="$4"

  local version
  version="$(jq -r '.tag_name | ltrimstr("v")' <<<"$releaseJson")"
  echo "=> ${pname}: ${version}" >&2

  local outFile="${SCRIPT_DIR}/packages/${pname}.nix"
  {
    echo '# Expression generated by update.sh; do not edit it by hand!'
    # `rec` so ${version} can be referenced from inside `archives` URLs.
    echo 'rec {'
    echo "  pname = \"${pname}\";"
    echo "  version = \"${version}\";"
    echo ''
    echo '  archives = {'
    emit_archive_entry "$releaseJson" "$version" "${debStem}_\${version}_arm64.deb"      "aarch64-linux"
    emit_archive_entry "$releaseJson" "$version" "${debStem}_\${version}_amd64.deb"      "x86_64-linux"
    emit_archive_entry "$releaseJson" "$version" "${darwinStem}-v\${version}-darwin-arm64.zip" "aarch64-darwin"
    emit_archive_entry "$releaseJson" "$version" "${darwinStem}-v\${version}-darwin-x64.zip"   "x86_64-darwin"
    echo '  };'
    echo '}'
  } > "$outFile"
}

# Stable: GitHub's /releases/latest endpoint always points at the newest
# non-prerelease, non-draft release, which matches the stable channel.
stableJson="$(curl --fail -s "${GH_AUTH[@]}" \
  "https://api.github.com/repos/${REPO}/releases/latest")"
write_package_nix "brave" "$stableJson" "brave-browser" "brave"
