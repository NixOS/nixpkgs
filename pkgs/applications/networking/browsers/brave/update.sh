#!/usr/bin/env nix-shell
#!nix-shell -i bash -p curl nix jq
# shellcheck shell=bash
#
# Regenerates pkgs/applications/networking/browsers/brave/packages/*.nix.
#
# Version and asset metadata comes from Brave's release index.

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" &>/dev/null && pwd)"

VERSIONS_URL="https://versions.brave.com/latest/brave-versions.json"

sri_hash() {
  nix-hash --to-sri --type sha256 "$(nix-prefetch-url --type sha256 "$1")"
}

# Finds the newest release in Brave's version index with a matching asset.
find_release_with_asset() {
  local versionsJson="$1" channel="$2" template="$3"

  local match
  match="$(
    jq -c --arg channel "$channel" --arg tmpl "$template" '
      [
        .[]
        | select(.channel == $channel)
        | . as $r
        | select(
            $r.github.assets
            | map(.name)
            | any(. == ($tmpl | gsub("\\$\\{version\\}"; $r.name)))
          )
      ]
      | sort_by(.published)
      | last
    ' <<<"$versionsJson"
  )"

  if [[ "$match" != "null" ]]; then
    printf '%s' "$match"
    return 0
  fi

  echo "update.sh: no ${channel} release with asset matching '${template}' found in ${VERSIONS_URL}" >&2
  return 1
}

# Emits a `<platform> = { url = ...; hash = ...; };` block if the release has
# an asset matching the given filename template. The generated Nix keeps
# `${version}` interpolations so subsequent bumps only refresh hashes.
#
# $1: release JSON (single object)
# $2: version (tag without leading "v")
# $3: asset filename template with literal ${version} placeholders
# $4: platform key to emit
emit_archive_entry() {
  local releaseJson="$1" version="$2" template="$3" platform="$4"
  local name="${template//\$\{version\}/$version}"

  local url
  url="$(
    jq -r --arg n "$name" '
      .github.assets[]
      | select(.name == $n)
      | .download_url
    ' <<<"$releaseJson"
  )"

  if [[ -z "$url" ]]; then
    return 0
  fi

  local hash
  hash="$(sri_hash "$url")"
  local nixUrl="${url//${version}/\$\{version\}}"

  cat <<EOF
    ${platform} = {
      url = "${nixUrl}";
      hash = "${hash}";
    };
EOF
}

# Writes pkgs/.../packages/<pname>.nix for one (pname, release) pair.
#
# $1: pname
# $2: release channel ("stable", "beta" or "nightly")
# $3: release JSON object
# $4: Linux .deb filename stem (e.g. "brave-browser")
# $5: macOS .zip filename stem (always "brave" for the regular browser; each
#     release tag uniquely identifies the channel)
write_package_nix() {
  local pname="$1" channel="$2" releaseJson="$3" debStem="$4" darwinStem="$5"

  local version
  version="$(jq -r '.name' <<<"$releaseJson")"
  echo "=> ${pname}: ${version}" >&2

  # Only emit `flavor` when it deviates from the make-brave.nix default
  # ("browser"), to keep browser-channel package.nix files tidy.
  local flavorLine=""
  if [[ "$pname" == brave-origin || "$pname" == brave-origin-* ]]; then
    flavorLine='  flavor = "origin";'
  fi

  local outFile="${SCRIPT_DIR}/packages/${pname}.nix"
  {
    echo '# Expression generated by update.sh; do not edit it by hand!'
    # `rec` so ${version} can be referenced from inside `archives` URLs.
    echo 'rec {'
    echo "  pname = \"${pname}\";"
    echo "  version = \"${version}\";"
    echo "  channel = \"${channel}\";"
    [[ -n "$flavorLine" ]] && echo "$flavorLine"
    echo ''
    echo '  archives = {'
    emit_archive_entry "$releaseJson" "$version" "${debStem}_\${version}_arm64.deb"      "aarch64-linux"
    emit_archive_entry "$releaseJson" "$version" "${debStem}_\${version}_amd64.deb"      "x86_64-linux"
    emit_archive_entry "$releaseJson" "$version" "${darwinStem}-v\${version}-darwin-arm64.zip" "aarch64-darwin"
    emit_archive_entry "$releaseJson" "$version" "${darwinStem}-v\${version}-darwin-x64.zip"   "x86_64-darwin"
    echo '  };'
    echo '}'
  } > "$outFile"
}

versionsJson="$(curl --fail -s "$VERSIONS_URL")"

# pname, channel, deb stem, darwin zip stem
channels=(
  "brave                release brave-browser         brave"
  "brave-beta           beta    brave-browser-beta    brave"
  "brave-nightly        nightly brave-browser-nightly brave"
  "brave-origin         release brave-origin          brave-origin"
  "brave-origin-beta    beta    brave-origin-beta     brave-origin"
  "brave-origin-nightly nightly brave-origin-nightly  brave-origin"
)

for entry in "${channels[@]}"; do
  read -r pname channel debStem darwinStem <<<"$entry"
  releaseJson="$(find_release_with_asset "$versionsJson" "$channel" "${debStem}_\${version}_amd64.deb")"
  packageChannel="$channel"
  [[ "$channel" == "release" ]] && packageChannel="stable"
  write_package_nix "$pname" "$packageChannel" "$releaseJson" "$debStem" "$darwinStem"
done
