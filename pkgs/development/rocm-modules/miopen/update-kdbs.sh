#!/usr/bin/env nix-shell
#!nix-shell -i bash -p curl jq nix
# shellcheck shell=bash

set -euo pipefail

gh_curl() {
  local -a auth_args=()

  if [[ -n ${GITHUB_TOKEN:-} ]]; then
    auth_args=(-u ":$GITHUB_TOKEN")
  fi

  curl -fsSL "${auth_args[@]}" "$@"
}

readonly version=${1:?usage: update-kdbs.sh VERSION}
readonly bucket=https://therock-dvc.s3.amazonaws.com/rocm-libraries/files/md5

cd "$(dirname "${BASH_SOURCE[0]}")"

mapfile -t dvc_specs < <(
  gh_curl "https://api.github.com/repos/ROCm/rocm-libraries/contents/projects/miopen/src/kernels?ref=rocm-${version}" \
    | jq -r '.[] | select(.name | endswith(".kdb.bz2.dvc")) | [.name, .download_url] | @tsv'
)

if (( ${#dvc_specs[@]} == 0 )); then
  echo "No .kdb.bz2.dvc files found for rocm-${version} in ROCm/rocm-libraries." >&2
  exit 1
fi

{
  echo "# Generated by update-kdbs.sh"
  echo "{"

  for dvc_spec in "${dvc_specs[@]}"; do
    IFS=$'\t' read -r dvc_file raw_url <<< "$dvc_spec"
    arch="${dvc_file%.kdb.bz2.dvc}"
    md5="$(gh_curl "$raw_url" | awk '/- md5:/{print $3}')"

    if [[ -z $md5 ]]; then
      echo "Could not extract md5 from $raw_url." >&2
      exit 1
    fi

    url="${bucket}/${md5:0:2}/${md5:2}"
    hash="$(nix-prefetch-url --type sha256 --name "${arch}.kdb.bz2" "$url")"
    sri_hash="$(nix-hash --to-sri --type sha256 "$hash")"

    echo "  $arch: $sri_hash" >&2
    cat <<EOF
  $arch = {
    url = "$url";
    hash = "$sri_hash";
  };
EOF
  done

  echo "}"
} > kdbs.nix
