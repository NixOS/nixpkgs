#!/usr/bin/env nix-shell
#! nix-shell -i bash --pure -p cacert curl jq nix xq-xml
# shellcheck shell=bash

set -euo pipefail

cd "$(readlink -e "$(dirname "${BASH_SOURCE[0]}")")"

# Ensure pager is unset for xq
unset PAGER

function darwin_data {
  curl \
    --fail-with-body \
    --silent \
    --show-error \
    https://d32ifkf9k9ezcg.cloudfront.net/production/sparkle/appcast.xml
}

function darwin_version {
  xq --xpath '//rss/channel/item[1]/sparkle:shortVersionString'
}

function darwin_url {
  xq --xpath '//rss/channel/item[1]/enclosure/@url'
}

function linux_data {
  local arch="${1}"

  curl \
    --fail-with-body \
    --silent \
    --show-error \
    --header 'Accept: application/tuple.app; version=1' \
    "https://production.tuple.app/latest_version/linux?arch=${arch}"
}

function linux_version {
  jq --raw-output '.version | ltrimstr("v") | gsub("_"; "-")'
}

function linux_url {
  jq --raw-output '.url'
}

function main {
  local DARWIN_DATA DARWIN_VERSION DARWIN_URL darwin_hash
  local AARCH64_LINUX_DATA AARCH64_LINUX_VERSION AARCH64_LINUX_URL aarch64_linux_hash
  local X86_64_LINUX_DATA X86_64_LINUX_VERSION X86_64_LINUX_URL x86_64_linux_hash

  DARWIN_DATA="$(darwin_data)"
  AARCH64_LINUX_DATA="$(linux_data arm64)"
  X86_64_LINUX_DATA="$(linux_data x86_64)"

  DARWIN_VERSION="$(darwin_version <<<"${DARWIN_DATA}")"
  AARCH64_LINUX_VERSION="$(linux_version <<<"${AARCH64_LINUX_DATA}")"
  X86_64_LINUX_VERSION="$(linux_version <<<"${X86_64_LINUX_DATA}")"

  DARWIN_URL="$(darwin_url <<<"${DARWIN_DATA}")"
  AARCH64_LINUX_URL="$(linux_url <<<"${AARCH64_LINUX_DATA}")"
  X86_64_LINUX_URL="$(linux_url <<<"${X86_64_LINUX_DATA}")"

  darwin_hash=$(nix-prefetch-url --unpack "${DARWIN_URL}")
  aarch64_linux_hash=$(nix-prefetch-url "${AARCH64_LINUX_URL}")
  x86_64_linux_hash=$(nix-prefetch-url "${X86_64_LINUX_URL}")

  # use friendlier hashes

  darwin_hash=$(nix --extra-experimental-features nix-command hash convert --to sri --hash-algo sha256 "${darwin_hash}")
  aarch64_linux_hash=$(nix --extra-experimental-features nix-command hash convert --to sri --hash-algo sha256 "${aarch64_linux_hash}")
  x86_64_linux_hash=$(nix --extra-experimental-features nix-command hash convert --to sri --hash-algo sha256 "${x86_64_linux_hash}")

  cat >sources.nix <<EOF
# Generated by ./update.sh - do not update manually!
# Last updated: $(date +%F)
{ fetchurl, fetchzip }:
{
  aarch64-darwin = {
    version = "${DARWIN_VERSION}";
    src = fetchzip {
      url = "${DARWIN_URL}";
      hash = "${darwin_hash}";
    };
  };
  x86_64-darwin = {
    version = "${DARWIN_VERSION}";
    src = fetchzip {
      url = "${DARWIN_URL}";
      hash = "${darwin_hash}";
    };
  };
  aarch64-linux = {
    version = "${AARCH64_LINUX_VERSION}";
    src = fetchurl {
      url = "${AARCH64_LINUX_URL}";
      hash = "${aarch64_linux_hash}";
    };
  };
  x86_64-linux = {
    version = "${X86_64_LINUX_VERSION}";
    src = fetchurl {
      url = "$X86_64_LINUX_URL";
      hash = "${x86_64_linux_hash}";
    };
  };
}
EOF
}

main "${@}"
