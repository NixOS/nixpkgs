#!/usr/bin/env nix-shell
#!nix-shell -i bash -p cabal2nix curl jq nixfmt nix-update

set -euo pipefail

# This is the directory of this update.sh script.
script_dir="$(dirname "${BASH_SOURCE[0]}")"

backend_derivation_file="${script_dir}/generated-backend-package.nix"

latest_backend_tag="$(curl --silent 'https://api.github.com/repos/gren-lang/compiler/releases/latest' | jq --raw-output '.tag_name')"

echo "Updating gren backend to version ${latest_backend_tag}."
echo "Running cabal2nix and outputting to ${backend_derivation_file}..."

cat > "${backend_derivation_file}" << EOF
# This file has been autogenerated with cabal2nix.
# Update via ./update.sh
EOF

cabal2nix 'https://github.com/gren-lang/compiler.git' --revision "${latest_backend_tag}" --jailbreak >> "${backend_derivation_file}"
nixfmt "${backend_derivation_file}"

echo 'Finished backend generation.'

# The frontend doesn't seem to be using the blob- prefixed tag
# so we strip it if its present
frontend_tag="${latest_backend_tag#blob-}"

nix-update gren --version="$frontend_tag"
